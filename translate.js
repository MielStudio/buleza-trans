  const langList = document.querySelectorAll(".lang-list a");
  const currentLang = document.getElementById("current-lang");
  
  const updateServicesAndAddress = (selectedLang) => {
    const servicesSectionTitle = translations[selectedLang].sections.services.title;
    const service1Text = translations[selectedLang].sections.services.service1;
    const service2Text = translations[selectedLang].sections.services.service2;
    
    const addressTitle = translations[selectedLang].sections.address.title;
    const addressText = translations[selectedLang].sections.address.text;

    document.querySelector(".section-title").textContent = servicesSectionTitle;
    const serviceTexts = document.querySelectorAll(".service-text");
    serviceTexts[0].textContent = service1Text;
    serviceTexts[1].textContent = service2Text;

    document.querySelector(".address-title").textContent = addressTitle;
    document.querySelector(".address-text").textContent = addressText;
    };

  langList.forEach((langLink) => {
    langLink.addEventListener("click", (e) => {
      e.preventDefault();
      const selectedLang = langLink.dataset.lang;

      if (selectedLang === activeLang) return;

        // Змінити текст на сторінці
        document.querySelector(".logo-top").textContent = translations[selectedLang].logoTop;
        document.querySelector(".logo-bottom").textContent = translations[selectedLang].logoBottom;

        const menuItems = document.querySelectorAll("#desktop-menu ul li a");
        menuItems.forEach((item, index) => {
        item.textContent = translations[selectedLang].menu[index];
        });

        const mobileMenuItems = document.querySelectorAll("#mobile-menu ul li a");
        mobileMenuItems.forEach((item, index) => {
        item.textContent = translations[selectedLang].menu[index];
        });
    
        // Змінюємо секції
        const homeSection = translations[selectedLang].sections.home;
        document.querySelector(".main-title").textContent = homeSection.title;
        document.querySelector(".main-text").textContent = homeSection.text;
        document.getElementById("openFormBtn").textContent = homeSection.button;
    
        const advantageSection = translations[selectedLang].sections.advantage;
        document.querySelector(".section-title").textContent = advantageSection.title;
        document.querySelectorAll(".section-text p")[0].textContent = advantageSection.text1;
        document.querySelectorAll(".section-text p")[1].textContent = advantageSection.text2;
    
        const stats = translations[selectedLang].sections.stats;
        document.querySelector(".statistic-left .statistic-top").textContent = stats.left.top;
        document.querySelector(".statistic-left .statistic-bottom").textContent = stats.left.bottom;
        document.querySelector(".statistic-center .statistic-top").textContent = stats.center.top;
        document.querySelector(".statistic-center .statistic-bottom").textContent = stats.center.bottom;
        document.querySelector(".statistic-right .statistic-top").textContent = stats.right.top;
        document.querySelector(".statistic-right .statistic-bottom").textContent = stats.right.bottom;
    
        // Оновлення активної мови
        currentLang.textContent = selectedLang.toUpperCase();
        activeLang = selectedLang;
    });
  });
  